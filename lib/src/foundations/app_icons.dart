import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';

/// La clase `AppIcons` proporciona una lista de íconos utilizados en la aplicación.
///
/// Esta clase define los íconos que se pueden utilizar en todo el sistema de
/// diseño, lo que garantiza la coherencia y facilita la actualización de los
/// íconos en el futuro.
class AppIcons {
  /// Ícono de búsqueda.
  static const IconData search = Icons.search;

  /// Ícono de bolsa de compras.
  static const IconData shoppingBag = CupertinoIcons.shopping_cart;

  /// Ícono de corazón delineado.
  static const IconData heartOutlined = CupertinoIcons.heart;

  /// Ícono de menú.
  static const IconData menu = Icons.menu;

  /// Ícono de flecha hacia atrás.
  static const IconData arrowBack = Icons.arrow_back;

  /// Ícono de estrella.
  static const IconData star = Icons.star;

  /// Ícono de más.
  static const IconData plus = Icons.add;

  /// Ícono de menos.
  static const IconData minus = Icons.remove;

  /// Ícono para cerrar.
  static const IconData close = Icons.close;

  /// Ícono de inicio.
  static const IconData home = Icons.home;

  /// Ícono de categoría.
  static const IconData category = Icons.category;

  /// Ícono de carrito de compras.
  static const IconData cart = Icons.shopping_cart;

  /// Ícono de perfil de usuario.
  static const IconData profile = Icons.person;
}
